package mmt.source.com.babitafuels.Model;

public class Bunk {
    private int bunkId;
    private String bunkArea;
    private String bunkAddr;
    private String bunkPincode;
    private String bunkName;
    private String bunkMngName;
    private String bunkMngMobile;
    private String bunkOfficeNum;
    private String action;

    public int getBunkId() {
        return bunkId;
    }

    public void setBunkId(int bunkId) {
        this.bunkId = bunkId;
    }

    public String getBunkArea() {
        return bunkArea;
    }

    public void setBunkArea(String bunkArea) {
        this.bunkArea = bunkArea;
    }

    public String getBunkAddr() {
        return bunkAddr;
    }

    public void setBunkAddr(String bunkAddr) {
        this.bunkAddr = bunkAddr;
    }

    public String getBunkPincode() {
        return bunkPincode;
    }

    public void setBunkPincode(String bunkPincode) {
        this.bunkPincode = bunkPincode;
    }

    public String getBunkName() {
        return bunkName;
    }

    public void setBunkName(String bunkName) {
        this.bunkName = bunkName;
    }

    public String getBunkMngName() {
        return bunkMngName;
    }

    public void setBunkMngName(String bunkMngName) {
        this.bunkMngName = bunkMngName;
    }

    public String getBunkMngMobile() {
        return bunkMngMobile;
    }

    public void setBunkMngMobile(String bunkMngMobile) {
        this.bunkMngMobile = bunkMngMobile;
    }

    public String getBunkOfficeNum() {
        return bunkOfficeNum;
    }

    public void setBunkOfficeNum(String bunkOfficeNum) {
        this.bunkOfficeNum = bunkOfficeNum;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }
}
