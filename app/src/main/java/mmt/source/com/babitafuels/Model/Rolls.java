package mmt.source.com.babitafuels.Model;

public class Rolls {
    private String usrId;
    private String usrProvider;
    private String accessPage;
    private String accessType;
    private String action;

    public String getUsrId() {
        return usrId;
    }

    public void setUsrId(String usrId) {
        this.usrId = usrId;
    }

    public String getUsrProvider() {
        return usrProvider;
    }

    public void setUsrProvider(String usrProvider) {
        this.usrProvider = usrProvider;
    }

    public String getAccessPage() {
        return accessPage;
    }

    public void setAccessPage(String accessPage) {
        this.accessPage = accessPage;
    }

    public String getAccessType() {
        return accessType;
    }

    public void setAccessType(String accessType) {
        this.accessType = accessType;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }
}
